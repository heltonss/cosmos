'use strict'

function Aluno(
    nome,
    sobrenome,
    dataNascimento,
    naturalidade,
    sexo,
    foto,
    nota,
    matricula,
    endereco,
    contato
) {
    this.nome = nome;
    this.sobrenome = sobrenome;
    this.dataNascimento = dataNascimento;
    this.naturalidade = naturalidade;
    this.sexo = sexo;
    this.foto = foto;
    this.nota = nota;
    this.matricula = matricula;
    this.endereco = endereco;
    this.contato = contato;
}